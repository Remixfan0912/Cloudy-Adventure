import pygame
from Settings import *

class Player(pygame.sprite.Sprite):

    def __init__(self, player_img, walk_player, jump_player, pop_sound, jump_sound):
        super().__init__()

        #* 玩家渲染
        self.image = pygame.transform.scale(player_img, (53, 45))
        self.rect = self.image.get_rect()
        self.rect.center = (WIDTH/2, 15)
        
        #* 玩家圖片、音檔
        self.walk_player = pygame.transform.scale(walk_player, (53, 45))
        self.jump_player = pygame.transform.scale(jump_player, (53, 45))
        self.pop_sound = pop_sound
        self.jump_sound = jump_sound
        
        #* 處理玩家位置、速度、加速度
        self.pos = [float(self.rect.centerx), float(self.rect.centery)]
        self.velocity = [0,0]
        self.acceleration = [PLAYER_ACCELERATION,0]
        
        #* 處理玩家能力數值
        self.player_jump_v = PLAYER_JUMP_V
        self.player_x_speed = PLAYER_X_SPEED
        self.g = G
        
        #* 玩家是否站在平台
        self.is_stand_on_platform = False
        
        #* 玩家能力增益
        self.buffList = [False, False, False, False]
        self.buffTimer = [0,0,0,0]
        
    def update(self, platform_group, pickup_group):
        # self.hitbox()
        self.position_change()
        self.timer_detect()
        self.key_event()
        self.gravity()
        self.check_platform_collide(platform_group)
        self.check_pickup_collide(pickup_group)
        self.animate()
        # self.border()
        
    def gravity(self):
        if self.is_stand_on_platform:
            return
        
        if self.rect.top >= HEIGHT:
            self.kill()
            return
        
        self.velocity[1] += self.g
        self.pos[1] += self.velocity[1]
        self.rect.centery = self.pos[1]
    
    def position_change(self):
        self.pos[0] += self.velocity[0]
        self.rect.centerx = self.pos[0]
        
    #! 偵測地面
    def border(self):
        if self.rect.bottom > HEIGHT:
            self.rect.bottom = HEIGHT

    def jump(self):
        self.jump_sound.play()
        self.velocity[1] += self.player_jump_v
        self.pos[1] += self.velocity[1]
        self.rect.centery = self.pos[1]
    
    def isAir(self):
        return not self.rect.bottom == HEIGHT

    def key_event(self):
        kp = pygame.key.get_pressed()
        if kp[pygame.K_LEFT]:
            self.pos[0] -= self.player_x_speed
            self.rect.centerx = self.pos[0]
        
        if kp[pygame.K_RIGHT]:
            self.pos[0] += self.player_x_speed
            self.rect.centerx = self.pos[0]
            
        if kp[pygame.K_UP]:
            if not self.isAir() or self.is_stand_on_platform:
                self.jump()
                
    def animate(self):
        kp = pygame.key.get_pressed()
                
        if kp[pygame.K_UP]:
            self.image = self.jump_player
            if kp[pygame.K_LEFT]:
                self.image = pygame.transform.flip(self.image, True, False)      
        else:      
            if kp[pygame.K_LEFT]:
                self.image = pygame.transform.flip(self.walk_player, True, False)
            
            if kp[pygame.K_RIGHT]:
                self.image = self.walk_player      
                
    def check_platform_collide(self, platform_group):
        self.is_stand_on_platform = False

        hits = pygame.sprite.spritecollide(self, platform_group, False)

        for platform in hits:
            if self.velocity[1] >= 0:
                self.rect.bottom = platform.rect.top   
                self.pos[1] = float(self.rect.centery) 
                self.velocity[1] = platform.velocity[1]        
                self.velocity[0] = platform.velocity[0]       
                self.is_stand_on_platform = True       
                break                                  
                        
    def check_pickup_collide(self, pickup_group):
        hits = pygame.sprite.spritecollide(self, pickup_group, True)
        for pickup in hits:
            if pickup.buff == 0:
                self.pop_sound.play()
                self.player_x_speed = PLAYER_X_SPEED*PICKUP_SPEED_RATIO
                self.buffList[0] = True
                self.buffTimer[0] = POWERUP_TIME*60
                
            if pickup.buff == 1:
                self.pop_sound.play()
                self.player_jump_v = PLAYER_JUMP_V*PICKUP_JUMPVELOCITY_RATIO
                self.buffList[1] = True
                self.buffTimer[1] = POWERUP_TIME*60
                
            if pickup.buff == 2:
                self.pop_sound.play()
                self.g = G*PICKUP_GRAVITY_RATIO
                self.buffList[2] = True
                self.buffTimer[2] = POWERUP_TIME*60
    
    def timer_detect(self):
        #* 玩家X速度增益1.5倍
        if self.buffList[0]:
            self.buffTimer[0] -= 1
            if self.buffTimer[0] <= 0:
                self.buffTimer[0] = 0
                self.buffList[0] = False
                self.player_x_speed = PLAYER_X_SPEED
        
        #* 玩家跳躍增益1.5倍
        if self.buffList[1]:
            self.buffTimer[1] -= 1
            if self.buffTimer[1] <= 0:
                self.buffTimer[1] = 0
                self.buffList[1] = False
                self.player_jump_v = PLAYER_JUMP_V
        
        #* 玩家掉落慢速增益
        if self.buffList[2]:
            self.buffTimer[2] -= 1
            if self.buffTimer[2] <= 0:
                self.buffTimer[2] = 0
                self.buffList[2] = False
                self.g = G
    
    #! player的碰撞箱顯示
    def hitbox(self):
        pygame.draw.rect(self.image, PURPLE, (0,0,self.rect.width, self.rect.height), 2)